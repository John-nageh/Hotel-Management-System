    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        if (!"".equals(first.getText()) && !"".equals(ssn.getText()) && !"".equals(members.getText()) && !r.emp() && !"".equals(paid.getText()) && !"".equals(middle.getText())
                 && !"".equals(last.getText()) && !"".equals(phone.getText())) {
            PreparedStatement stmt;
            try {
                stmt = con.prepareStatement("insert into clients(first_name, middle_name, last_name, phone_number, ssn, number_in_reservation, paid, check_in_date)"
                        + " values (?,?,?,?,?,?,?,?)");
                stmt.setString(1, first.getText());
                stmt.setString(2, middle.getText());
                stmt.setString(3, last.getText());
                stmt.setString(4, phone.getText());
                stmt.setString(5, ssn.getText());
                stmt.setString(6, members.getText());
                stmt.setString(7, paid.getText());
                java.util.Date currentDate = new java.util.Date();
                Date sqlDate = new Date(currentDate.getTime());
                stmt.setDate(8, sqlDate);
                
                stmt.execute();
                
                stmt = con.prepareStatement("select client_id from clients where ssn = ?");
                stmt.setString(1, ssn.getText());
                ResultSet rs = stmt.executeQuery();
                int id = 0;
                while (rs.next()) {
                    id = rs.getInt(1);
                }
                ArrayList<Integer> rr = r.getI();
                for (int i = 0; i < rr.size(); i++) {
                    stmt = con.prepareStatement("UPDATE room SET client_id = ? WHERE room_number = ?");
                    stmt.setString(1, String.valueOf(id));
                    stmt.setString(2, String.valueOf(rr.get(i)));
                    stmt.execute();
                }
            } catch (SQLException ex) {
                Logger.getLogger(reservation.class.getName()).log(Level.SEVERE, null, ex);
            }
            r.clr();
            r.fi = "";
            r.mi = "";
            r.la = "";
            r.ph = "";
            r.mm = "";
            r.du = "";
            r.ss = "";
            r.pd = "";
            
            this.dispose();
            new options().setVisible(true);
        }
    }//GEN-LAST:event_jButton3ActionPerformed

