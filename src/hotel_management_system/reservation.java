    private void fillTable() {
        t = new DefaultTableModel();
        t.addColumn("Room Number");
        t.addColumn("price/night");
        ArrayList<Integer> rr = r.getI();
        ArrayList<Double> rrp = r.getD();
        for (int i = 0; i < rr.size(); i++) {
            t.addRow(new Object[]{rr.get(i), rrp.get(i)});
        }
        rrooms.setModel(t);
    }

