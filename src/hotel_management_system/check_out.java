    private void fillTable() {
        try {
            t = new DefaultTableModel();
            t.addColumn("id");
            t.addColumn("name");
            t.addColumn("paid");
            t.addColumn("date");
            t.setRowCount(0);
            PreparedStatement stmt = con.prepareStatement("select client_id, first_name, last_name, paid, check_in_date from clients");
            ResultSet rs = stmt.executeQuery();
            while (rs.next()) {
                t.addRow(new Object[]{rs.getInt(1), rs.getString(2) + " " + rs.getString(3), rs.getDouble(4), rs.getDate(5)});
            }
            checkout.setModel(t);
        } catch (SQLException ex) {
            Logger.getLogger(check_out.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

