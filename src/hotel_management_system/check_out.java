    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        if (!"".equals(client.getText()) && !"".equals(dur.getText())) {
            PreparedStatement stmt;
            try {
                double ans = 0;
                stmt = con.prepareStatement("select price_per_night from room where client_id = ?");
                stmt.setString(1, client.getText());
                ResultSet rs = stmt.executeQuery();
                while (rs.next()) {
                    ans += rs.getDouble(1);
                }
                reserved ob = new reserved();
                ob.fpr = (ans * Integer.valueOf(dur.getText()));
                ob.id = Integer.valueOf(client.getText());
                ob.pa = Double.valueOf(paid.getText());
            } catch (SQLException ex) {
                Logger.getLogger(check_out.class.getName()).log(Level.SEVERE, null, ex);
            }
            this.dispose();
            new final_price().setVisible(true);
        }
    }//GEN-LAST:event_jButton3ActionPerformed

